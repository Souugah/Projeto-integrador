    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        btnPesquisar.addActionListener(new ActionListener() {
            private Iterable<Cliente> resultados;
    public void actionPerformed(ActionEvent e) {
        String nomeClientePesquisa = jTextField1.getText();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); 
        for (Cliente cliente : resultados) {
            model.addRow(new Object[] { cliente.getId(), cliente.getNome(), cliente.getTelefone(), cliente.getCpf(), cliente.getEndereco(), cliente.getNomePet() });
        }
    }
        });
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        btnExcluir.addActionListener(new ActionListener() {
    public void actionPerformed(ActionEvent e) {
        int linhaSelecionada = jTable2.getSelectedRow();

        if (linhaSelecionada == -1) {
            
            JOptionPane.showMessageDialog(null, "Selecione um cliente ou pet para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            int idClienteParaExcluir = (int) jTable2.getValueAt(linhaSelecionada, 0);

           
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.removeRow(linhaSelecionada);
        }
    }
});
    }//GEN-LAST:event_btnExcluirActionPerformed

