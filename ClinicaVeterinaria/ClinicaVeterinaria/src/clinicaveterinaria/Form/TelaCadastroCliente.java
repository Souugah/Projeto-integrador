    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        btnCadastrar.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                String nomeCliente = jTextField1.getText();
                String enderecoCliente = jTextField2.getText();
                String cpfCliente = jFormattedTextField1.getText();
                String bairroCliente = jTextField4.getText();
                String estadoCliente = jTextField5.getText();
                String telefoneCliente = jFormattedTextField3.getText();

                Cliente cliente = new Cliente(nomeCliente, enderecoCliente, cpfCliente, bairroCliente, estadoCliente, telefoneCliente);

                ConexaoMySQL cSQL = new ConexaoMySQL();

                ClienteDao clienteDao = new ClienteDao(cSQL.getConnection());
                clienteDao.adicionarCliente(cliente);

                // Exibir mensagem de sucesso
                JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso!");
                
                
                limparCampos();

                // Atualizar a tabela na FormCliente
                FormClienteView.preencherTabela();
            }
        });

    }//GEN-LAST:event_btnCadastrarActionPerformed

