     private void preencherTabela() {
        // Crie uma instância do PetDao para obter a lista de pets
        PetDao petDao = new PetDao(new ConexaoMySQL().getConnection());

        // Obtenha a lista de pets do banco de dados
        List<Pet> listaPets = petDao.listarPets();

        // Obtenha o modelo da tabela
        DefaultTableModel tabelaPets = (DefaultTableModel) jTable1.getModel();

        // Limpe a tabela antes de preenchê-la para evitar duplicatas
        tabelaPets.setRowCount(0);

        for (Pet pet : listaPets) {
            Object[] obj = new Object[] {
                pet.getId(),
                pet.getNome(),
                pet.getRaca(),
                pet.getEspecie(),
                pet.getDataNascimento(),
                pet.getSexo()
            };
            tabelaPets.addRow(obj);
        }
    }
    
    

    
